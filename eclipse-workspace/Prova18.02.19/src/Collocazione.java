
public class Collocazione {

	private String settore;
	private int codSett;
	
	public Collocazione() {
		settore= "";
		codSett= 0;
	}
	
	public Collocazione(String settore, int codSett) {
		this.settore= settore;
		this.codSett= codSett;
	}
	
	public String getSettore() {
		return settore;
	}
	
	public int codSettore() {
		return codSett;
	}
	
	
	
}
